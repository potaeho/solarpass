export type GridLine = {
  id: string;
  name: string;
  voltage: 765 | 345 | 154;
  coordinates: { latitude: number; longitude: number }[];
  capacityMW: number;
  usedMW: number;
  province: string[];
};

export type GridSaturation = 'surplus' | 'caution' | 'shortage' | 'saturated';

export const getGridSaturation = (used: number, capacity: number): GridSaturation => {
  const ratio = used / capacity;
  if (ratio < 0.5)  return 'surplus';
  if (ratio < 0.75) return 'caution';
  if (ratio < 0.9)  return 'shortage';
  return 'saturated';
};

